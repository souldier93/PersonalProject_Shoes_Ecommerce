const shoeDetailSchema = new mongoose.Schema({}, { collection: 'shoesDetail', strict: false });

const Shoe = mongoose.model('SeedShoe', shoeSchema);
const ShoeDetail = mongoose.model('SeedShoeDetail', shoeDetailSchema);

async function seed() {
  await mongoose.connect(MONGO_URI);

  for (const product of products) {
    const firstColor = product.colors[0];
    const detail = {
      productId: product.productId,
      name: product.name,
      category: product.category,
      productType: product.productType,
      collection: product.collection,
      price: product.price,
      colors: product.colors.map((color) => ({
        ...color,
        category: product.category,
        productType: product.productType,
        collection: product.collection,
        createdAt: now,
        updatedAt: now,
      })),
    };

    await ShoeDetail.updateOne(
      { productId: product.productId },
      { $set: detail },
      { upsert: true },
    );

    await Shoe.updateOne(
      { productId: product.productId },
      {
        $set: {
          productId: product.productId,
          name: product.name,
          category: product.category,
          productType: product.productType,
          collection: product.collection,
          price: firstColor.price,
          color: firstColor.colorName,
          thumbnail: firstColor.thumbnail,
        },
      },
      { upsert: true },
    );

    console.log(`Seeded ${product.productId} - ${product.name}`);
  }

  await mongoose.disconnect();
  console.log(`Done. Seeded ${products.length} Nike-inspired products.`);
}

seed().catch(async (error) => {
  console.error(error);
  await mongoose.disconnect();
  process.exit(1);
});
